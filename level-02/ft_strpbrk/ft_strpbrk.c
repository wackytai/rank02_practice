char	*ft_strpbrk(const char *s1, const char *s2)
{
	int		i;
	int		j;
	char	*nptr;

	i = 0;
	if (!s1 || !s2)
		return (0);
	while (s1[i] != '\0')
	{
		j = 0;
		while (s2[j] != '\0')
		{
			if (s1[i] == s2[j])
			{
				nptr = &s1[i];
				return (&nptr);
			}
			j++;
		}
		i++;
	}
	return (0);
}
