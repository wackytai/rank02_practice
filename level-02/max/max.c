int	max(int *tab, unsigned int len)
{
	unsigned int	i;
	int				max;	

	i = 0;
	max = tab[i];
	if (tab[i] == '\0')
		return (0);
	while (i < len)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
